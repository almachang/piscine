#include<stdio.h>

int	ft_sqrt(int nb)
{
	int	n;

	n = 1;
	while (n * n <= nb && n < 46341)
	{
		if (n * n == nb)
		{
			return (n);
		}
		else
		{
			n++;
		}
	}
	return (0);
}
